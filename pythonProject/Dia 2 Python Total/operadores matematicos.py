#Suma resta y multiplicación
x = 6
y = 2
z = 7
print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")
print(f"{x} dividido {y} es igual a {x/y}")

#division al piso  es una operacion que arroja un numero decimal pero se elimina el decimal en el resultado se representa con //
print(f"{z} dividido al piso de {y} es igual a {z//y}")
#modulo es el sobrante de una operacon con decimales ej se usa mas en programacion para detectar numero pares e impares
print(f"{z} modulo de {y} es igual a {z%y}")
#ahora con elevaciones se usa * representando la cantidad de veces a la cual se elevas **=2 ***=3
print(f"{x} elevado a la {y} es igual a {x**y}")
#tambien puedes incluir numeros para la operacion
print(f"{x} elevado a la {3} es igual a {x**3}")
#para sacar raiz cuadrada de cualquier valor se usa **0.5
print(f"la raiz cuadrada de {x} es {x**0.5}")
