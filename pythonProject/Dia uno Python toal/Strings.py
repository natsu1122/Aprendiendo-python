#\" añade comillas dentro de un print
print("Me llamo \"Nelson\"")
#\n separa lineas de texto una de la otra
print("Esta es la primer linea \nEsta es la segunda linea \nEsta es la tercer linea")
#\t añade un espacio de tabulador
print("aqui \testa el tabulado")
#\' añade el caracter al texto donde lo incluyas
print("This isn\'t a number")
#\\ agrega la barra invertida en el texto
print("esta es una barra invertida \\")
#En resumen \ es un strings que ayuda a agregar caracteres a una cadena de texto
